import java.util.Date;

public class User {

    String Name;

    Date UserDate;

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }

    public Date getUserDate() {
        return UserDate;
    }

    public void setUserDate(Date userDate) {
        UserDate = userDate;
    }
}
